<template>
    <div :id="modal.id" class="modal">
        <div class="modal-content">
            <slot name="content">
        </div>
        <div class="modal-footer">
            <slot name="footer">
        </div>
    </div>
</template>

<script>
    export default {
        props: {
            modal: {
                type: Object,
                default () {
                    return {
                        id: ""
                    }
                }
            }
        },
        ready () {
            $(document).ready(() => {
                $(`#${this.modal.id}`).modal();
            });
        }
    };
</script>
